def check_vowels():
    name1 = input() 

    print("Contiene a:", ("a" in name1.lower()))
    print("Contiene e:", ("e" in name1.lower()))
    print("Contiene i:", ("i" in name1.lower()))
    print("Contiene o:", ("o" in name1.lower()))
    print("Contiene u:", ("u" in name1.lower()))
    



check_vowels()